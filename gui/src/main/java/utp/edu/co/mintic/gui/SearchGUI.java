    private void botBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botBuscarActionPerformed
        
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String fechainicial = sdf.format(FechaIni.getDate());
        String fechafinal = sdf.format(FechaFin.getDate());
        
        String[] nombresdeColumna = {"Fecha","Ciudad","Constructora","Lider","CÃ³digo","Estrato"};
        Controlador controladortabla = new Controlador();
        ArrayList<Requerimiento2vo> lista;
        
        try{
            lista = controladortabla.consulta2(fechainicial,fechafinal,textoCiudad.getText());
            Object[][] matrizdedatos = new Object[lista.size()][6];
            int indice = 0;
            for(Requerimiento2vo x: lista){
                matrizdedatos[indice][0] = x.getFecIni();
                matrizdedatos[indice][1] = x.getNombreCiudad();
                matrizdedatos[indice][2] = x.getConstructora();
                matrizdedatos[indice][3] = x.getNombreLider();
                matrizdedatos[indice][4] = x.getCodigoTipo();
                matrizdedatos[indice][5] = x.getEstrato();
                indice++;
            }
            DefaultTableModel modelodeDatos = new DefaultTableModel(matrizdedatos,nombresdeColumna);
            tablareport2.setModel(modelodeDatos);
        } catch(SQLException ex){
            System.err.println(ex);
        }
    }//GEN-LAST:event_botBuscarActionPerformed

